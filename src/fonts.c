/* ===================================================================
 *  G1 - Mosaiques semi-graphiques 2x3 blocs
 *
 *  Chaque caractere code un motif de 2 colonnes x 3 lignes.
 *  Le code $20-$7F encode les 6 bits du motif:
 *    bit 0 = haut-gauche
 *    bit 1 = haut-droit
 *    bit 2 = milieu-gauche
 *    bit 3 = milieu-droit
 *    bit 4 = bas-gauche
 *    bit 5 = bas-droit
 *
 *  Chaque bloc = 3x3 pixels (haut), 3x3 (milieu), 3x2 (bas)
 *  Total: 6x8 pixels par cellule.
 * =================================================================== */

/* Buffer statique pour le glyphe G1 genere */
static unsigned char g1_glyph[8];

static const unsigned char* generate_g1(unsigned char code)
{
    unsigned char pattern;
    unsigned char left, right;
    unsigned char line_byte;
    unsigned char i;

    /* Extraire le motif 6 bits */
    pattern = code & 0x3F;

    /* Generer les 8 lignes */
    /* Lignes 0-2: rangee haute (bits 0,1) */
    left  = (pattern & 0x01) ? 0x38 : 0x00;  /* 3 pixels gauche: bits 5,4,3 */
    right = (pattern & 0x02) ? 0x07 : 0x00;  /* 3 pixels droit: bits 2,1,0 */
    line_byte = left | right;
    for (i = 0; i < 3; ++i) {
        g1_glyph[i] = line_byte;
    }

    /* Lignes 3-5: rangee milieu (bits 2,3) */
    left  = (pattern & 0x04) ? 0x38 : 0x00;
    right = (pattern & 0x08) ? 0x07 : 0x00;
    line_byte = left | right;
    for (i = 3; i < 6; ++i) {
        g1_glyph[i] = line_byte;
    }

    /* Lignes 6-7: rangee basse (bits 4,5) */
    left  = (pattern & 0x10) ? 0x38 : 0x00;
    right = (pattern & 0x20) ? 0x07 : 0x00;
    line_byte = left | right;
    g1_glyph[6] = line_byte;
    g1_glyph[7] = line_byte;

    return g1_glyph;
}

