const unsigned char* font_get_g0(unsigned char ch)
{
    if (ch < 0x20 || ch > 0x7F) {
        ch = 0x20;  /* espace par defaut */
    }
    return &font_g0[(ch - 0x20) * 8];
}

const unsigned char* font_get_g1(unsigned char ch)
{
    return generate_g1(ch);
}

